# 4-10 Slices
numbers = list(range(1, 21))
# Print first three
print(f"The first three numbers are {numbers[0:3]}")
# Print middle three (theres probably a better way of doing this than manually calculating the three middle numbers in the range)
print(f"The three middle numbers are {numbers[9:12]}")
# Print last three
print(f"The last three numbers are {numbers[-3:]}")
